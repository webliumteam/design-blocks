import $editor from 'weblium/editor'

class Block extends React.Component {
  static propTypes = {
    components: PropTypes.object.isRequired,
    $block: PropTypes.object.isRequired,
    style: PropTypes.object.isRequired,
  }

  getModifierValue = path => _.get(['modifier', path], this.props.$block)

  collectionItem = ({index, children, className, modifier, openLightbox}) => {
    const {
      components: {Text, Image},
      style,
    } = this.props
    return (
      <div className={classNames(style.item, className)}>
        {children}
        <Image
          wrapperClassName={classNames(style['item__picture-wrapper'], 'picture-wrapper')}
          pictureClassName={classNames(style.item__picture, 'picture')}
          imgClassName={classNames(style.item__image, 'picture__image')}
          bind={`gallery[${index}].picture`}
          size={{
            'min-width: 320px': 480,
            'min-width: 480px': 768,
            'min-width: 768px': 310,
            'min-width: 992px': 380,
          }}
          onOpenLightbox={openLightbox}
        />
        {_.get('body-text')(modifier) && (
          <Text
            tagName="p"
            className={classNames(style.item__text, 'body')}
            bind={`gallery[${index}].title`}
          />
        )}
        {_.get('caption')(modifier) && (
          <Text
            tagName="p"
            className={classNames(style.item__caption, 'caption')}
            bind={`gallery[${index}].caption`}
          />
        )}
      </div>
    )
  }

  render() {
    const {
      components: {Collection, Text, Button, Icon},
      style,
      $block,
    } = this.props
    return (
      <section className={style.section}>
        <div className={style.section__inner}>
          {this.getModifierValue('top-icon') && (
            <Icon className={classNames(style['top-icon'], 'icon')} bind="topIcon" />
          )}
          {(this.getModifierValue('title') || this.getModifierValue('subtitle')) && (
            <header className={style.section__header}>
              {this.getModifierValue('title') && (
                <Text
                  tagName="h1"
                  className={classNames(style.title, 'title', 'text-center')}
                  bind="title"
                />
              )}
              {this.getModifierValue('subtitle') && (
                <Text
                  tagName="p"
                  className={classNames(style.subtitle, 'subtitle', 'text-center')}
                  bind="subtitle"
                />
              )}
            </header>
          )}
          <Collection
            className={style['items-wrapper']}
            bind="gallery"
            galleryId="gallery"
            Item={this.collectionItem}
            itemProps={{
              modifier: $block.modifier,
            }}
          />
          {this.getModifierValue('secondary-button') && (
            <div className={classNames(style['btns-group'], 'btns-group')}>
              <div className={classNames(style['btns-group__inner'], 'btns-group__inner')}>
                <Button
                  className={classNames(style.button, 'button')}
                  linkClassName={style.link}
                  bind="cta"
                />
              </div>
            </div>
          )}
        </div>
      </section>
    )
  }
}

Block.components = _.pick(['Collection', 'Text', 'Button', 'Image', 'Icon'])($editor.components)

